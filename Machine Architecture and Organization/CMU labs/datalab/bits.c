/* 
 * sm2tc - Convert from sign-magnitude to two's complement
 *   where the MSB is the sign bit
 *   Example: sm2tc(0x80000005) = -5.
 *   Legal ops: ! ~ & ^ | + << >>
 *   Max ops: 15
 *   Rating: 4
 */
int sm2tc(int x) {
  int s = x >> 31;
  int t = (0x1<<31)+1;
  int m = t&s;
  return (x^s)+m; // -x = ~x +100..01    -x = ~x +1 and in this case we need to add sign 
}
